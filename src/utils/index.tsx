// Função para pegar uma carta do dia
export function getCardOfTheDay() {
  const randomIndex = Math.floor(Math.random() * CARDS.length);
  return CARDS[randomIndex];
}

// Função para pegar três cartas
export function getThreeCards() {
  const shuffledCards = CARDS.sort(() => 0.5 - Math.random());
  return shuffledCards.slice(0, 3);
}

// Função para pegar cinco cartas
export function getFiveCards() {
  const shuffledCards = CARDS.sort(() => 0.5 - Math.random());
  return shuffledCards.slice(0, 5);
}

// Função para pegar todas as cartas
export function getAllCards() {
  return CARDS;
}

export function getGreeting(): string {
  const now = new Date();
  const hours = now.getHours();

  if (hours >= 0 && hours < 12) {
    return 'Bom dia';
  } else if (hours >= 12 && hours < 18) {
    return 'Boa tarde';
  } else {
    return 'Boa noite';
  }
}
